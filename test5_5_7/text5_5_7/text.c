//写一个宏可以将一个数字的二进制的奇数位和偶数位交换。

#include<stdio.h>
#include<stdlib.h>

#define SWAP_BIT(n) (((n&0xaaaaaaa)>>1) | ((n&0x55555555)<<1))

int main()
{
	int a = 10;
	//000000000000000000000000001010  --> 10
	//000000000000000000000000000101  --> 5
	//给偶数位按位与1 拿出偶数位，右移1
	//101010101010101010101010101010    1010-->十六进制是a
	//给奇数位按位与1 拿出奇数位，左移1
	//010101010101010101010101010101    0101-->十六进制是5
	//最后把两个按位或
	a = SWAP_BIT(a);
	printf("%d\n", a);
	system("pause");
	return 0;
}
